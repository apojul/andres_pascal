<template>
  <v-navigation-drawer
      v-model="drawer"
      app
      max-width="250"
      :mini-variant.sync="mini"
      color="pink lighten-4"
    >
      <v-list nav class="overfow-y-auto" max-height="400">
        <v-list-item class="px-0">
          <v-list-item-avatar>
            <v-img src="https://cdn.vuetifyjs.com/images/lists/1.jpg" />
          </v-list-item-avatar>

          <v-list-item-title>{{activeUser.full_name}}</v-list-item-title>

          <v-btn icon @click.stop="mini = !mini">
            <v-icon>mdi-chevron-left</v-icon>
          </v-btn>
        </v-list-item>
        <v-divider />
        <v-list-group sub-group active-class="white--text text--accent-4">
          <template #activator>
            <v-list-item-title>Board List</v-list-item-title>
          </template>
          <v-list-item
            v-for="board in boardList"
            :key="board.id"
            dark
            :to="{ name: 'boardId', params: { id: board.id } }"
          >
            <v-list-item-content>
              <v-list-item-title>{{ board.name }}</v-list-item-title>
            </v-list-item-content>
          </v-list-item>
        </v-list-group>
        <v-list-group
          no-action
          sub-group
          active-class="grey--text text--accent-4"
        >
          <v-list-item-content>
            <v-list-item-title>Actions</v-list-item-title>
          </v-list-item-content>
          <v-list-item>
            <AddBoard />
          </v-list-item>
        </v-list-group>
      </v-list>
  </v-navigation-drawer>
</template>

<script>
import AddBoard from '@/components/AddBoard'
export default {
    name: 'BoardDrawer',
    components: {
        AddBoard
    },
    data (){
      return {
        mini: true,
        drawer: true
      }
    },
    computed: {
    boardList() {
      if (this.$store.state.boards === undefined) {
        this.$store.dispatch('fetch_board_list')
        return []
      }
      return this.$store.state.boards
    },
    activeUser() {
      if (this.$store.state.activeUser === undefined) {
        return {}
      }
      return this.$store.state.activeUser
    },
  }

}
</script>

